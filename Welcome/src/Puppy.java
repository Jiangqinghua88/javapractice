
public class Puppy {
	int puppyAge;
	public Puppy(String name){
		System.out.println("小狗的名字是："+ name);
	}
	public void setAge(int age){
		puppyAge = age;
	}
	public void getAge(){
		System.out.println("小狗的年龄为："+ puppyAge);
	}
	public static void main(String[] args){
		/* 创建对象 */
		Puppy myPuppy = new Puppy("tommy");
		myPuppy.setAge(2);
		myPuppy.getAge();
		
	}

}
